package com.ai.reference;

import java.io.Serializable;

/**
 * Runtime Semantic check thrown if {@link ASTCell#execute()} finds an invalid type matching.
 * @author Martin Tice
 *
 */
public class TypeException extends Exception implements Serializable{

	/**
	 * Prints a given string when throwing exception
	 * @param s string to print when exception is thrown.
	 */
	public TypeException(String s) {
		super(s);
	}

	/**
	 * ex: "no numeric value for cell "qumquat""
	 * this is type1, this is type2, types not equal.
	 */
	private static final long serialVersionUID = -4497816400067288560L;

}
