package com.game.characters;

/**
 * Enum representing the five different animations a player can take.
 * Subject to change
 * 
 * @author Ian
 */
public enum CharAnimations {
	/** The attack animation directory. */
	ATTACK("attack"),
	/** The die animation directory. */
	DIE("die"),
	/** The shoot animation directory. */
	SHOOT("shoot"),
	/** The stand animation directory. */
	STAND("stand"),
	/** The walk animation directory. */
	WALK("walk");
	
	private CharAnimations(String subdir) {
		this.subdir = subdir;
	}
	
	/**
	 * The animation sub-directory that holds
	 * all images for animations.
	 */
	public final String subdir;
}
