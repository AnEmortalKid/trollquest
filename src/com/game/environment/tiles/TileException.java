package com.game.environment.tiles;

/**
 * @author TBworkstation
 *
 */
public class TileException extends Exception {
  /** TileException serial id */
  private static final long serialVersionUID = 6727295293793753746L;

  /**
   * Returns a Tile exception with the appropriate message. This should be thrown
   * if there was an issue interacting with a tile 
   * @param errorMessage the message to be displayed or passed on
   */
  public TileException(String errorMessage) {
    super(errorMessage);
  }

}
